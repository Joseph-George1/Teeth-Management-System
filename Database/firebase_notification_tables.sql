/**
 * FIREBASE NOTIFICATION SYSTEM - CLEAN TABLE CREATION
 * 
 * This script DROPS and RECREATES notification tables from scratch
 * Database: Oracle XE (orclpdb)
 * 
 * OPERATIONS:
 * 1. Drops existing DEVICE_TOKEN table
 * 2. Drops existing NOTIFICATION_LOG table
 * 3. Creates fresh DEVICE_TOKEN table
 * 4. Creates fresh NOTIFICATION_LOG table
 * 5. Creates sequences and triggers
 * 6. Creates indexes
 * 
 * WARNING: This will delete all existing data in these tables!
 */

-- ============================================================================
-- STEP 1: DROP EXISTING TABLES (and all dependencies)
-- ============================================================================

DROP TABLE NOTIFICATION_PREFERENCES;
DROP TABLE NOTIFICATION_LOGS;
DROP TABLE DEVICE_TOKENS;
DROP SEQUENCE SEQ_DEVICE_TOKEN_ID;
DROP SEQUENCE SEQ_NOTIFICATION_LOG_ID;
DROP SEQUENCE SEQ_NOTIFICATION_PREFERENCE_ID;

-- ============================================================================
-- STEP 2: CREATE DEVICE_TOKENS TABLE
-- ============================================================================

CREATE TABLE DEVICE_TOKENS (
    ID                  NUMBER(19) PRIMARY KEY,
    TOKEN               VARCHAR2(500 CHAR) NOT NULL UNIQUE,
    USER_ID             NUMBER(19) NOT NULL,
    USER_TYPE           VARCHAR2(20 CHAR) NOT NULL,
    PLATFORM            VARCHAR2(20 CHAR) NOT NULL,
    DEVICE_NAME         VARCHAR2(255 CHAR),
    IS_ACTIVE           NUMBER(1) DEFAULT 1 NOT NULL,
    REGISTERED_AT       TIMESTAMP DEFAULT SYSDATE NOT NULL,
    LAST_USED_AT        TIMESTAMP,
    DEACTIVATED_AT      TIMESTAMP
);

COMMENT ON TABLE DEVICE_TOKENS IS 'Firebase Cloud Messaging device tokens for push notifications';
COMMENT ON COLUMN DEVICE_TOKENS.ID IS 'Unique device token ID (auto-generated)';
COMMENT ON COLUMN DEVICE_TOKENS.TOKEN IS 'Unique FCM device token from Firebase SDK';
COMMENT ON COLUMN DEVICE_TOKENS.USER_ID IS 'User ID who owns this device token (Patient, Doctor, or Admin ID)';
COMMENT ON COLUMN DEVICE_TOKENS.USER_TYPE IS 'Type of user: PATIENT, DOCTOR, or ADMIN';
COMMENT ON COLUMN DEVICE_TOKENS.PLATFORM IS 'Device platform: ANDROID, IOS, WEB, WINDOWS, MACOS';
COMMENT ON COLUMN DEVICE_TOKENS.DEVICE_NAME IS 'Human-readable device name (e.g., iPhone 13, Samsung Galaxy S21)';
COMMENT ON COLUMN DEVICE_TOKENS.IS_ACTIVE IS 'Whether token is currently active (1=yes, 0=no)';
COMMENT ON COLUMN DEVICE_TOKENS.REGISTERED_AT IS 'Timestamp when token was registered';
COMMENT ON COLUMN DEVICE_TOKENS.LAST_USED_AT IS 'Timestamp when token was last used';
COMMENT ON COLUMN DEVICE_TOKENS.DEACTIVATED_AT IS 'Timestamp when token was deactivated (soft delete)';

-- ============================================================================
-- STEP 3: CREATE NOTIFICATION_LOGS TABLE
-- ============================================================================

CREATE TABLE NOTIFICATION_LOGS (
    ID                      NUMBER(19) PRIMARY KEY,
    RECIPIENT_USER_ID       NUMBER(19) NOT NULL,
    RECIPIENT_USER_TYPE     VARCHAR2(20 CHAR) NOT NULL,
    TITLE                   VARCHAR2(255 CHAR) NOT NULL,
    BODY                    VARCHAR2(1000 CHAR) NOT NULL,
    NOTIFICATION_TYPE       VARCHAR2(50 CHAR) NOT NULL,
    RELATED_ENTITY_ID       NUMBER(19),
    RELATED_ENTITY_TYPE     VARCHAR2(50 CHAR),
    FCM_MESSAGE_ID          VARCHAR2(255 CHAR),
    DELIVERY_STATUS         VARCHAR2(20 CHAR) DEFAULT 'SENT' NOT NULL,
    IS_READ                 NUMBER(1) DEFAULT 0 NOT NULL,
    READ_AT                 TIMESTAMP,
    DATA_PAYLOAD            VARCHAR2(2000 CHAR),
    SENT_AT                 TIMESTAMP DEFAULT SYSDATE NOT NULL,
    CREATED_AT              TIMESTAMP DEFAULT SYSDATE NOT NULL
);

COMMENT ON TABLE NOTIFICATION_LOGS IS 'Audit trail of all notifications sent to users';
COMMENT ON COLUMN NOTIFICATION_LOGS.ID IS 'Unique notification log ID (auto-generated)';
COMMENT ON COLUMN NOTIFICATION_LOGS.RECIPIENT_USER_ID IS 'User ID who received the notification';
COMMENT ON COLUMN NOTIFICATION_LOGS.RECIPIENT_USER_TYPE IS 'Type of recipient: PATIENT, DOCTOR, ADMIN';
COMMENT ON COLUMN NOTIFICATION_LOGS.TITLE IS 'Notification title/subject';
COMMENT ON COLUMN NOTIFICATION_LOGS.BODY IS 'Notification message body';
COMMENT ON COLUMN NOTIFICATION_LOGS.NOTIFICATION_TYPE IS 'Notification type (APPOINTMENT_CONFIRMED, CANCELLED, REMINDER, etc.)';
COMMENT ON COLUMN NOTIFICATION_LOGS.RELATED_ENTITY_ID IS 'Related entity ID (e.g., Appointment ID, Request ID)';
COMMENT ON COLUMN NOTIFICATION_LOGS.RELATED_ENTITY_TYPE IS 'Type of related entity (APPOINTMENT, REQUEST, BOOKING, etc.)';
COMMENT ON COLUMN NOTIFICATION_LOGS.FCM_MESSAGE_ID IS 'FCM message ID for tracking delivery status';
COMMENT ON COLUMN NOTIFICATION_LOGS.DELIVERY_STATUS IS 'Delivery status (PENDING, SENT, DELIVERED, FAILED, BOUNCED, EXPIRED)';
COMMENT ON COLUMN NOTIFICATION_LOGS.IS_READ IS 'Whether user read notification (1=yes, 0=no)';
COMMENT ON COLUMN NOTIFICATION_LOGS.READ_AT IS 'Timestamp when user read the notification';
COMMENT ON COLUMN NOTIFICATION_LOGS.DATA_PAYLOAD IS 'Additional data payload in JSON format';
COMMENT ON COLUMN NOTIFICATION_LOGS.SENT_AT IS 'Timestamp when notification was sent';
COMMENT ON COLUMN NOTIFICATION_LOGS.CREATED_AT IS 'Timestamp when record was created';

-- ============================================================================
-- STEP 4: CREATE NOTIFICATION_PREFERENCES TABLE
-- ============================================================================

CREATE TABLE NOTIFICATION_PREFERENCES (
    ID                                      NUMBER(19) PRIMARY KEY,
    USER_ID                                 NUMBER(19) NOT NULL UNIQUE,
    USER_TYPE                               VARCHAR2(20 CHAR) NOT NULL,
    PUSH_NOTIFICATIONS_ENABLED              NUMBER(1) DEFAULT 1 NOT NULL,
    APPOINTMENT_CONFIRMED_ENABLED           NUMBER(1) DEFAULT 1 NOT NULL,
    APPOINTMENT_CANCELLED_ENABLED           NUMBER(1) DEFAULT 1 NOT NULL,
    APPOINTMENT_REMINDER_ENABLED            NUMBER(1) DEFAULT 1 NOT NULL,
    BOOKING_REQUEST_ENABLED                 NUMBER(1) DEFAULT 1 NOT NULL,
    SYSTEM_ANNOUNCEMENT_ENABLED             NUMBER(1) DEFAULT 1 NOT NULL,
    PROMOTIONAL_ENABLED                     NUMBER(1) DEFAULT 0 NOT NULL,
    QUIET_HOURS_START                       NUMBER(2),
    QUIET_HOURS_END                         NUMBER(2),
    ALLOW_NOTIFICATIONS_IN_QUIET_HOURS      NUMBER(1) DEFAULT 0 NOT NULL,
    LANGUAGE_PREFERENCE                     VARCHAR2(10 CHAR) DEFAULT 'en',
    EMAIL_NOTIFICATIONS_ENABLED             NUMBER(1) DEFAULT 0 NOT NULL,
    SMS_NOTIFICATIONS_ENABLED               NUMBER(1) DEFAULT 0 NOT NULL,
    DAILY_NOTIFICATION_LIMIT                NUMBER(3) DEFAULT 0,
    UPDATED_AT                              TIMESTAMP DEFAULT SYSDATE NOT NULL,
    CREATED_AT                              TIMESTAMP DEFAULT SYSDATE NOT NULL
);

COMMENT ON TABLE NOTIFICATION_PREFERENCES IS 'User preferences for receiving notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.ID IS 'Unique preference ID (auto-generated)';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.USER_ID IS 'User ID (Patient, Doctor, or Admin)';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.USER_TYPE IS 'Type of user: PATIENT, DOCTOR, ADMIN';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.PUSH_NOTIFICATIONS_ENABLED IS 'Whether push notifications are globally enabled';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.APPOINTMENT_CONFIRMED_ENABLED IS 'Enable appointment confirmation notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.APPOINTMENT_CANCELLED_ENABLED IS 'Enable appointment cancellation notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.APPOINTMENT_REMINDER_ENABLED IS 'Enable appointment reminder notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.BOOKING_REQUEST_ENABLED IS 'Enable booking request notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.SYSTEM_ANNOUNCEMENT_ENABLED IS 'Enable system announcement notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.PROMOTIONAL_ENABLED IS 'Enable promotional/marketing notifications';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.QUIET_HOURS_START IS 'Start hour of quiet period (24-hour format, 0-23)';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.QUIET_HOURS_END IS 'End hour of quiet period (24-hour format, 0-23)';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.ALLOW_NOTIFICATIONS_IN_QUIET_HOURS IS 'Allow notifications during quiet hours';
