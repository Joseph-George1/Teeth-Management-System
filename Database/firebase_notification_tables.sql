COMMENT ON COLUMN NOTIFICATION_PREFERENCES.DAILY_NOTIFICATION_LIMIT IS 'Max notifications per day (0 = unlimited)';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.UPDATED_AT IS 'Timestamp when preferences were last updated';
COMMENT ON COLUMN NOTIFICATION_PREFERENCES.CREATED_AT IS 'Timestamp when preferences were created';

CREATE SEQUENCE SEQ_DEVICE_TOKEN_ID
    START WITH 1
    INCREMENT BY 1
    NOCYCLE;

CREATE SEQUENCE SEQ_NOTIFICATION_LOG_ID
    START WITH 1
    INCREMENT BY 1
    NOCYCLE;

CREATE SEQUENCE SEQ_NOTIFICATION_PREFERENCE_ID
    START WITH 1
    INCREMENT BY 1
    NOCYCLE;

-- ============================================================================
-- STEP 5: CREATE TRIGGERS FOR AUTO-INCREMENT
-- ============================================================================

CREATE OR REPLACE TRIGGER DEVICE_TOKEN_ID_TRIGGER
BEFORE INSERT ON DEVICE_TOKENS
FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT SEQ_DEVICE_TOKEN_ID.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
    IF :NEW.REGISTERED_AT IS NULL THEN
        :NEW.REGISTERED_AT := SYSDATE;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER NOTIFICATION_LOG_ID_TRIGGER
BEFORE INSERT ON NOTIFICATION_LOGS
FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT SEQ_NOTIFICATION_LOG_ID.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
    IF :NEW.SENT_AT IS NULL THEN
        :NEW.SENT_AT := SYSDATE;
    END IF;
    IF :NEW.CREATED_AT IS NULL THEN
        :NEW.CREATED_AT := SYSDATE;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER NOTIFICATION_PREFERENCE_ID_TRIGGER
BEFORE INSERT ON NOTIFICATION_PREFERENCES
FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT SEQ_NOTIFICATION_PREFERENCE_ID.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
    IF :NEW.CREATED_AT IS NULL THEN
        :NEW.CREATED_AT := SYSDATE;
    END IF;
    :NEW.UPDATED_AT := SYSDATE;
END;
/

CREATE OR REPLACE TRIGGER NOTIFICATION_PREFERENCE_UPDATE_TRIGGER
BEFORE UPDATE ON NOTIFICATION_PREFERENCES
FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSDATE;
END;
/

-- ============================================================================
