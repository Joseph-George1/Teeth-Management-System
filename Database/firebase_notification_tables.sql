CREATE INDEX IDX_DEVICE_TOKEN_IS_ACTIVE ON DEVICE_TOKENS(IS_ACTIVE);

CREATE INDEX IDX_NOTIFICATION_LOG_RECIPIENT_USER ON NOTIFICATION_LOGS(RECIPIENT_USER_ID);
CREATE INDEX IDX_NOTIFICATION_LOG_IS_READ ON NOTIFICATION_LOGS(IS_READ);
CREATE INDEX IDX_NOTIFICATION_LOG_CREATED_AT ON NOTIFICATION_LOGS(CREATED_AT);
CREATE INDEX IDX_NOTIFICATION_LOG_UNREAD ON NOTIFICATION_LOGS(RECIPIENT_USER_ID, IS_READ);
CREATE INDEX IDX_NOTIFICATION_LOG_TYPE ON NOTIFICATION_LOGS(NOTIFICATION_TYPE);
CREATE INDEX IDX_NOTIFICATION_LOG_RELATED_ENTITY ON NOTIFICATION_LOGS(RELATED_ENTITY_ID, RELATED_ENTITY_TYPE);

CREATE INDEX IDX_NOTIFICATION_PREF_USER_ID ON NOTIFICATION_PREFERENCES(USER_ID);
CREATE INDEX IDX_NOTIFICATION_PREF_USER_TYPE ON NOTIFICATION_PREFERENCES(USER_TYPE);

